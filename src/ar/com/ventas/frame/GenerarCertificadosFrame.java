    private void llenarTabla() {
        UtilFrame.limpiarTabla(tabla);
        trabajos = null;
        try {
            trabajos = new TrabajoService().getTrabajosByCertificadosPendientes();
        } catch (Exception ex) {
            Logger.getLogger(GenerarCertificadosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (trabajos != null && !trabajos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Trabajo trabajo : trabajos) {
                Object o[] = new Object[2];
                o[0] = sdf.format(trabajo.getFecha());
                Domicilio dm = trabajo.getServicio().getConsorcio().getDomicilio();
                o[1] = dm.getCalle() + " " + dm.getNumero();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void pdf() {
        int row = tabla.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN TRABAJO PARA GENERAR CERTIFICADO");
            return;
        }
        Trabajo trabajo = trabajos.get(row);
        int pdfGenerado = 0;
        try {
            File pdf = new PDFBuilder().armarCertificado2(trabajo);
            DesktopApi.open(pdf);
            pdfGenerado = 1;
        } catch (DocumentException ex) {
            Logger.getLogger(GenerarCertificadosFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception ex) {
            Logger.getLogger(GenerarCertificadosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (pdfGenerado == 1) {
            trabajo.setPdfGenerado(true);
            try {
                new TrabajoService().updateTrabajo(trabajo);
            } catch (Exception ex) {
                Logger.getLogger(GenerarCertificadosFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        JOptionPane.showMessageDialog(this, "CERTIFICADO GENERADO");
        llenarTabla();
    }
