    private void llenarTabla() {
        limpiarTabla();
        estructuras = null;
        try {
            estructuras = new EstructuraService().getAllEstructurasInactivas();
        } catch (Exception ex) {
            Logger.getLogger(EstructurasInactivasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (estructuras != null && !estructuras.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Estructura e : estructuras) {
                Object o[] = new Object[3];
                o[0] = e.getNombre();
                o[1] = e.getObservaciones();
                if (e.getActivo()) {
                    o[2] = "ACTIVO";
                } else {
                    o[2] = "INACTIVO";
                }
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void limpiarTabla() {
        int rows = tabla.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (int i = 0; i < rows; i++) {
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }

    private void volver() {
        CrearEstructuraFrame cef = new CrearEstructuraFrame();
        cef.setVisible(true);
        this.dispose();
    }

    private void activar() {
        int row = tabla.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "SELECCIONE UNA ESTRUCTURA PARA ACTIVAR");
            return;
        }
        Estructura es = estructuras.get(row);
        es.setActivo(true);
        try {
            new EstructuraService().updateEstructura(es);
            JOptionPane.showMessageDialog(this, "ESTRUCTURA ACTIVADA CORRECTAMENTE");
        } catch (Exception ex) {
            Logger.getLogger(EstructurasInactivasFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "ERROR Nro. 218 ACTIVANDO ESTRUCTURA");
            return;
        }
        volver();
    }
