    private void pdf() {
        int row = tabla.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FACTURA PARA GENERAR PDF");
            return;
        }
        Comprobante fc = facturas.get(row);
        TitularCuit titular = new TitularCuit();
        Persona persona = new Persona();
        persona.setApellidoNombre(fc.getRazonSocialTitular());
        titular.setCuit(fc.getCuitTitular());
        titular.setSucursal(fc.getSucursal());
        titular.setPersona(persona);
        Consorcio consorcio = null;
        Integer numeroFactura = fc.getNumero();
        Long cae = fc.getCae();
        Date fechaCae = fc.getFechaVencimientoCae();
        Double total = fc.getTotal();
        Date fecha = fc.getFecha();
        NuevoCae nc = new NuevoCae();
        
        try {
            File pdf = new PDFBuilder().armarFacturaC(titular,
                    consorcio, tabla, numeroFactura, nc,
                    total);
            DesktopApi.open(pdf);
        } catch (DocumentException ex) {
            Logger.getLogger(FacturaReparacion2Frame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "ERROR 279");
        } catch (Exception ex) {
            Logger.getLogger(FacturaReparacion2Frame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "ERROR 282");
        }
        volver();
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void buscar() {
        Date de = new Date();
        Date al = new Date();
        try {
            de = sdf.parse(deTxt.getText());
            al = sdf.parse(alTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(VerIvaVentasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        facturas = null;
        UtilFrame.limpiarTabla(tabla);
        try {
            facturas = new ComprobanteService().getComprobantesEntrFechas(de, al);
        } catch (Exception ex) {
            Logger.getLogger(VerIvaVentasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (facturas != null && !facturas.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Comprobante fc : facturas) {
                Object o[] = new Object[5];
                o[0] = sdf.format(fc.getFecha());
                o[1] = fc.getNumero();
                o[2] = fc.getRazonSocialTitular();
                o[3] = fc.getCalleNroPisoDtoCliente();
                o[4] = df.format(fc.getTotal());
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }
