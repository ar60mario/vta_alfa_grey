    private void nuevo() {
        int rowC = comboC.getSelectedIndex();
        if (rowC > 0) {
            NuevoTrabajoFrame nsf = new NuevoTrabajoFrame();
            nsf.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN CONSORCIO");
        }
    }

    private void llenarComboC() {
        comboT.removeAllItems();
        comboT.addItem("");
        comboC.removeAllItems();
        comboC.addItem("");
        consorcios = null;
        try {
            consorcios = new ConsorcioService().getAllConsorciosActivos();
        } catch (Exception ex) {
            Logger.getLogger(NuevoTrabajoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (consorcios != null && !consorcios.isEmpty()) {
            for (Consorcio c : consorcios) {
                comboC.addItem(c.getNombre() + " "
                        + c.getDomicilio().getCalle() + " "
                        + c.getDomicilio().getNumero());
            }
        }
    }

    private void llenarComboT() {
        int row = comboC.getSelectedIndex();
        if (row > 0) {
            comboT.removeAllItems();
            comboT.addItem("");
            Consorcio consorcio = consorcios.get(row - 1);
            trabajos = null;
            try {
                //trabajos = new ServicioService().getAllTrabajosActivosByConsorcio(consorcio);
            } catch (Exception ex) {
                Logger.getLogger(AbmServicios1Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (trabajos != null && !trabajos.isEmpty()) {
                for (Servicio t : trabajos) {
                    comboT.addItem(t.getRubro().getDetalle());
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN CONSORCIO PARA VER LOS TRABAJOS");
            return;
        }
    }

    private void llenarTabla(int rowC, int rowT) {
        Consorcio consorcio = consorcios.get(rowC - 1);
        Servicio trabajo = trabajos.get(rowT - 1);
        servicios = null;
        try {
            //servicios = new TrabajoService().getAllServiciosActivos();
        } catch (Exception ex) {
            Logger.getLogger(AbmServicios1Frame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (servicios != null && !servicios.isEmpty()) {
            UtilFrame.limpiarTabla(tabla);
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Trabajo s : servicios) {
                Object o[] = new Object[4];
                o[0] = s.getFecha();
                //o[1] = s.getTrabajo().getRubro().getDetalle();
                //o[2] = s.getTrabajo().getCantidadCuotas();
                o[3] = s.getTextoEnFactura();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }
