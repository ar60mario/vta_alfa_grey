    private void llenarTabla() {
        UtilFrame.limpiarTabla(tabla);
        fondos = null;
        try {
            fondos = new FondoReciboService().getAllFondoRecibosInactivos();
        } catch (Exception ex) {
            Logger.getLogger(FondosReciboEliminadosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (fondos != null && !fondos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (FondoRecibo fr : fondos) {
                Object o[] = new Object[3];
                o[0] = fr.getEmpresa();
                o[1] = fr.getUbicacion();
                o[2] = fr.getTitular().getPersona().getApellidoNombre();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void volver() {
        AbmFondosRecibosFrame afrf = new AbmFondosRecibosFrame();
        afrf.setVisible(true);
        this.dispose();
    }

    private void activar(int row) {
        FondoRecibo fr = fondos.get(row);
        fr.setActivo(true);
        int a = JOptionPane.showConfirmDialog(this, "CONFIRMA ACTIVARLO?", "Atenci√≥n", JOptionPane.YES_NO_OPTION);
        if(a==0){
            try {
                new FondoReciboService().updateFondoRecibo(fr);
            } catch (Exception ex) {
                Logger.getLogger(FondosReciboEliminadosFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR 195");
                return;
            }
            JOptionPane.showMessageDialog(this, "ACTIVADO");
            llenarTabla();
        }
    }
