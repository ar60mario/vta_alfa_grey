    private void llenar() {
        UtilFrame.limpiarTabla(tabla);
        abonos = null;
        try {
            abonos = new AbonoService().getAllAbonosActivos();
        } catch (Exception ex) {
            Logger.getLogger(ReconstruirAbonoFacturaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
        for (Abono ab : abonos) {
            Integer codCons = ab.getConsorcio().getCodigo();
            Consorcio cons = null;
            try {
                cons = new ConsorcioService().getConsorcioByCodigo(codCons);
            } catch (Exception ex) {
                Logger.getLogger(ReconstruirAbonoFacturaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            Domicilio dm = cons.getDomicilio();
            Rubro ru = ab.getRubro();
            String calle = dm.getCalle()
                    + dm.getNumero();
            Object o[] = new Object[7];
            o[0] = calle;
            o[1] = cons.getAdministrador().getNombreAdministrador();
            o[2] = ab.getId();
            o[3] = ru.getDetalle();

            Comprobante comp = null;
            try {
                comp = new ComprobanteService().getUltimoComprobanteByConsorcio(cons);
            } catch (Exception ex) {
                Logger.getLogger(ReconstruirAbonoFacturaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (comp != null) {
                o[4] = comp.getId();
                o[5] = comp.getNumero();
                String cuit = comp.getCuitTitular();
                TitularCuit tc = null;
                try {
                    tc = new TitularCuitService().getTitularActivoByCuit(cuit);
                } catch (Exception ex) {
                    Logger.getLogger(ReconstruirAbonoFacturaFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
                o[6] = tc.getPersona().getApellidoNombre();
            }
            tbl.addRow(o);
        }
        tabla.setModel(tbl);
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void marcarComoInactivo(int row) {
        Abono a = abonos.get(row);
        a.setActivo(false);
        int x = JOptionPane.showConfirmDialog(this, "CONFIRME ELIMINAR\n" + a.getRubro().getDetalle(), "Atenci√≥n", JOptionPane.YES_NO_OPTION);
        if (x == 0) {
            try {
                new AbonoService().updateAbono(a);
            } catch (Exception ex) {
                Logger.getLogger(ReconstruirAbonoFacturaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            llenar();
        }
    }
