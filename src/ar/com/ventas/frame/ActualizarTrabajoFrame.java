    private void llenarListas() {
        tabla.requestFocus();
        consorcioTxt.setEditable(false);
        servicioTxt.setEditable(false);
        limpiarCampos();
        Servicio servicio = trabajo.getServicio();
        Consorcio consorcio = servicio.getConsorcio();
        Domicilio dom = consorcio.getDomicilio();
        String conso = dom.getCalle() + " " + dom.getNumero();
        consorcioTxt.setText(conso);
        servicioTxt.setText(servicio.getRubro().getDetalle());
        fechaCargar = trabajo.getFecha();
        cargarRenglones();
    }

