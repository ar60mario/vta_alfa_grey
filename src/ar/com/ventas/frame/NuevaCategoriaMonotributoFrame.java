    private void guardar() {
        if (validar()) {
            CategoriaMonotributo categoria = new CategoriaMonotributo();
            categoria.setActivo(true);
            categoria.setCategoria(letraTxt.getText());
            categoria.setFechaActualizacion(new Date());
            Double importe = Double.valueOf(importeTxt.getText().replace(",", "."));
            categoria.setIngresosBrutos(importe);
            try {
                new CategoriaMonotributoService().saveCategoriaMonotributo(categoria);
                JOptionPane.showMessageDialog(this, "CATEGORIA GUARDADA CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(NuevaCategoriaMonotributoFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro. 177 . GUARDANDO CATEGORIA");
                return;
            }
            volver();
        }
    }

    private void volver() {
        AbmMonotributoFrame amf = new AbmMonotributoFrame();
        amf.setVisible(true);
        this.dispose();
    }

    private boolean validar() {
        String letra = letraTxt.getText();
        int existe = 0;
        CategoriaMonotributo ct = null;
        try {
            ct = new CategoriaMonotributoService().getCategoriaByLetraActivo(letra);
            if (ct != null) {
                if (ct.getCategoria().equals(letra)) {
                    existe = 1;
                } else {
                    existe = 0;
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(NuevaCategoriaMonotributoFrame.class.getName()).log(Level.SEVERE, null, ex);
            existe = 0;
        }
        if (existe == 1) {
            JOptionPane.showMessageDialog(this, "CATEGORIA MONOTRIBUTO EXISTENTE\nVERIFIQUE E INGRESE UNA NUEVA");
            letraTxt.requestFocus();
            return false;
        }
        if (importeTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UN IMPORTE");
            importeTxt.requestFocus();
            return false;
        }
        return true;
    }

    private void limpiarCampos() {
        letraTxt.setText("");
        importeTxt.setText("");
    }
