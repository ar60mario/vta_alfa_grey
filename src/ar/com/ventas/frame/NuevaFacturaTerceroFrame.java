    private void guardar() {
        if(validar()){
            FacturaDeTercero fdt = new FacturaDeTercero();
            fdt.setCobrada(false);
            fdt.setConsorcio(consorcio);
            fdt.setEntregada(false);
            Date fecha = new Date();
            try {
                fecha = sdf.parse(fechaTxt.getText());
            } catch (ParseException ex) {
                Logger.getLogger(NuevaFacturaTerceroFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro 266 FECHA FACTURA\nUTILICE DD/MM/AAAA");
                return;
            }
            fdt.setFecha(fecha);
            Double pesos = Double.valueOf(totalTxt.getText());
            fdt.setTotal(pesos);
            fdt.setImporteCobrado(0.0);
            fdt.setProveedor(proveedorTxt.getText());
            Integer numero = Integer.valueOf(numeroTxt.getText());
            fdt.setNumero(numero);
            try {
                new FacturaDeTerceroService().saveFacturaDeTercero(fdt);
                JOptionPane.showMessageDialog(this, "FACTURA GUARDADA CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(NuevaFacturaTerceroFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro.281 GUARDANDO FACTURA");
                return;
            }
            volver();
        }
    }

    private void volver() {
        FacturasTerceroFrame ftf = new FacturasTerceroFrame();
        ftf.setVisible(true);
        this.dispose();
    }

    private void limpiarCampos() {
        consorcioTxt.setText(consorcio.getNombre() + " "
                + consorcio.getDomicilio().getCalle() + " "
                + consorcio.getDomicilio().getNumero());
        consorcioTxt.setEditable(false);
        proveedorTxt.setText("");
        totalTxt.setText("");
        fechaTxt.setText("");
        numeroTxt.setText("");
        fechaTxt.requestFocus();
    }

    private boolean validar() {
        if(fechaTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "DEBE ESCRIBIR UNA FECHA");
            fechaTxt.requestFocus();
            return false;
        }
        if(numeroTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "DEBE ESCRIBIR UN NUMERO DE FACTURA");
            numeroTxt.requestFocus();
            return false;
        }
        if(proveedorTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "DEBE ESCRIBIR UN PROVEEDOR DE FACTURA");
            proveedorTxt.requestFocus();
            return false;
        }
        if(totalTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "DEBE ESCRIBIR UN TOTAL DE FACTURA");
            totalTxt.requestFocus();
            return false;
        }
        return true;
    }
