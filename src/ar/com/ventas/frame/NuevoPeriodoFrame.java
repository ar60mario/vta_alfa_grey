    private void limpiarCampos() {
        fechaTxt.setText(sdf.format(new Date()));
        rubros = null;
        try {
            rubros = new RubroService().getAllRubrosActivos();
        } catch (Exception ex) {
            Logger.getLogger(NuevoPeriodoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(rubros != null && !rubros.isEmpty()){
            comboR.removeAllItems();
            comboR.addItem("");
            for(Rubro ru:rubros){
                comboR.addItem(ru.getDetalle());
            }
        }
    }

