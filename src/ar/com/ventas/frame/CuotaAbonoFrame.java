    private void llenarTabla() {
        int row = combo.getSelectedIndex();
        Rubro ru = rubros.get(row - 1);
        int cuota = Integer.valueOf(comboN.getSelectedItem().toString());
//        cuotaTxt.setText("");
        abonos = null;
        try {
            abonos = new AbonoService().getAbonosByRubroAndCuota(ru, cuota);
        } catch (Exception ex) {
            Logger.getLogger(CuotaAbonoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        UtilFrame.limpiarTabla(tabla);
        if (abonos != null && !abonos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Abono ab : abonos) {
                if (ab.getConsorcio() != null) {
                    if (ab.getConsorcio().getDomicilio() != null) {
                        Domicilio dm = ab.getConsorcio().getDomicilio();
                        if (dm.getCalle() != null) {
                            if (dm.getNumero() != null) {
                                String calle = dm.getCalle() + " " + dm.getNumero();
                                Object o[] = new Object[3];
                                o[0] = calle;
                                o[1] = ab.getCuotaFacturada();
                                o[2] = ab.getImporte() / ab.getCuotas();
                                tbl.addRow(o);
                            }
                        }
                    }
                }
            }
            tabla.setModel(tbl);
        }
    }

