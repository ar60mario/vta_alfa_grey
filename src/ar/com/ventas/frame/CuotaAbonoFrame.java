    private void llenarCombos() {
        combo.removeAllItems();
        comboN.removeAllItems();
        combo.addItem("");
        comboN.addItem("");
        for (Integer i = 0; i < 7; i++) {
            comboN.addItem(i.toString());
        }
        rubros = null;
        try {
            rubros = new RubroService().getAllRubrosActivos();
        } catch (Exception ex) {
            Logger.getLogger(CuotaAbonoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        for (Rubro ru : rubros) {
            combo.addItem(ru.getDetalle());
        }
    }
