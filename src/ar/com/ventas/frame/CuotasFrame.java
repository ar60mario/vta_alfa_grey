    private void llenarTabla() {
        UtilFrame.limpiarTabla(tabla);
        Date de = new Date();
        Date al = new Date();
        try {
            de = sdf.parse(deTxt.getText());
            al = sdf.parse(alTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(CuotasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        comprobantes = null;
        try {
            comprobantes = new ComprobanteService().getComprobantesEntrFechas(de, al);
        } catch (Exception ex) {
            Logger.getLogger(CuotasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(comprobantes != null && !comprobantes.isEmpty()){
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for(Comprobante co:comprobantes){
                Object o[]  = new Object[3];
                o[0]=co.getCalleNroPisoDtoCliente();
                o[1]=co.getCuotasPagadas();
                o[2]=co.getCantidadCuotas();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void limpiarCampos() {
        numeroTxt.setText("");
        deTxt.setText("");
        alTxt.setText("");
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void asignar(int row) {
        Comprobante com = comprobantes.get(row);
        Integer cta = Integer.valueOf(numeroTxt.getText());
        com.setCuotasPagadas(cta);
        try {
            new ComprobanteService().updateComprobante(com);
        } catch (Exception ex) {
            Logger.getLogger(CuotasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        llenarTabla();
    }
