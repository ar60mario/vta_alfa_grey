    private void llenarTabla() {
        limpiarTabla();
        importes = null;
        try {
            importes = new ImporteService().getAllImportesInactivos();
        } catch (Exception ex) {
            Logger.getLogger(ImportesInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (importes != null && !importes.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Importe p : importes) {
                Object o[] = new Object[2];
                o[0] = p.getDetalle();
                o[1] = df.format(p.getImporte());
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void limpiarTabla() {
        int rows = tabla.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (int i = 0; i < rows; i++) {
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }

    private void modificar() {
        int row = tabla.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN IMPORTE DE TRABAJO PARA ACTIVAR");
            return;
        }
        Importe importe = importes.get(row);
        importe.setActivo(true);
        int a = JOptionPane.showConfirmDialog(this, "CONFIRME ACTIVAR\n" + importe.getDetalle(),
                 "AtenciÃ³n", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            try {
                new ImporteService().updateImporte(importe);
                JOptionPane.showMessageDialog(this, "IMPORTE DE TRABAJO ACTIVADO CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(ImportesInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro.218 AL ACTIVAR IMPORTE DE TRABAJO");
                return;
            }
            volver();
        }
    }

    private void volver() {
        AbmImportesFrame mf = new AbmImportesFrame();
        mf.setVisible(true);
        this.dispose();
    }
