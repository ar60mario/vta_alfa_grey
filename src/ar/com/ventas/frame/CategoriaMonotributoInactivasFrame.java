    private void llenarTabla() {
        UtilFrame.limpiarTabla(tabla);
        categorias = null;
        try {
            categorias = new CategoriaMonotributoService().getAllCategoriasMonotributoInactivos();
        } catch (Exception ex) {
            Logger.getLogger(CategoriaMonotributoInactivasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (categorias != null && !categorias.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (CategoriaMonotributo cm : categorias) {
                Object o[] = new Object[4];
                o[0] = cm.getCategoria();
                o[1] = df.format(cm.getIngresosBrutos());
                if (cm.getActivo()) {
                    o[2] = "ACTIVO";
                } else {
                    o[2] = "INACTIVO";
                }
                o[3] = sdf.format(cm.getFechaActualizacion());
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void activar() {
        int row = tabla.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN CATEGORIA PARA ACTIVAR");
            return;
        }
        CategoriaMonotributo cm = categorias.get(row);
        cm.setActivo(true);
        if (!existe()) {
            int a = JOptionPane.showConfirmDialog(this, "CONFIRME ACTIVAR CATEORIA \n" + cm.getCategoria(), "Atención", JOptionPane.YES_NO_OPTION);
            if (a == 0) {
                try {
                    new CategoriaMonotributoService().updateCategoriaMonotributo(cm);
                    JOptionPane.showMessageDialog(this, "CATEGORIA ACTUALIZADA CORRECTAMENTE");
                } catch (Exception ex) {
                    Logger.getLogger(CategoriaMonotributoInactivasFrame.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "ERROR Nro.257 ACTUALIZANDO CATEGORIA");
                    return;
                }
                volver();
            }
        }
    }

    private void volver() {
        AbmMonotributoFrame mf = new AbmMonotributoFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private boolean existe() {
        int row = tabla.getSelectedRow();
        DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
        String letra = tbl.getValueAt(row, 0).toString();
        int existe = 0;
        CategoriaMonotributo ct = null;
        try {
            ct = new CategoriaMonotributoService().getCategoriaByLetraActivo(letra);
            if (ct != null) {
                if (ct.getCategoria().equals(letra)) {
                    existe = 1;
                } else {
                    existe = 0;
                }
            }
        } catch (Exception ex) {
            Logger.getLogger(NuevaCategoriaMonotributoFrame.class.getName()).log(Level.SEVERE, null, ex);
            existe = 0;
        }
        if (existe == 1) {
            JOptionPane.showMessageDialog(this, "CATEGORIA MONOTRIBUTO EXISTENTE\nVERIFIQUE Y SELECCIONE UNA NUEVA");
            return true;
        }
        return false;
    }
