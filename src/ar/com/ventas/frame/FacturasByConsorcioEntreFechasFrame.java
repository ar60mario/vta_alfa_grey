    private void llenarCombo() {
        combo.removeAllItems();
        combo.addItem("");
        consorcios = null;
        String filtro = filtroTxt.getText();
        try {
            consorcios = new ConsorcioService().getAllConsorciosActivosByFiltro(filtro);
        } catch (Exception ex) {
            Logger.getLogger(FacturasByConsorcioEntreFechasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (consorcios != null && !consorcios.isEmpty()) {
            for (Consorcio co : consorcios) {
                Domicilio dm = co.getDomicilio();
                String nombre = dm.getCalle() + " " + dm.getNumero();
                combo.addItem(nombre);
            }
            combo.addFocusListener(null);
            combo.showPopup();
            combo.requestFocus();
        }
    }

    private void buscarConsorcio(Consorcio consorcio) {
        Date de = new Date();
        Date al = new Date();
        try {
            de = sdf.parse(deTxt.getText());
            al = sdf.parse(alTxt.getText());
        } catch (ParseException ex) {
            Logger.getLogger(FacturasByConsorcioEntreFechasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        comprobantes = null;
        try {
            comprobantes = new ComprobanteService().getComprobantesEntreFechasByConsorcio(consorcio, de, al);
        } catch (Exception ex) {
            Logger.getLogger(FacturasByConsorcioEntreFechasFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        UtilFrame.limpiarTabla(tabla);
        if (comprobantes != null && !comprobantes.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Comprobante co : comprobantes) {
                Object o[] = new Object[5];
                o[0] = sdf.format(co.getFecha());
                o[1] = co.getNumero();
                o[2] = co.getRazonSocialTitular();
                o[3] = co.getRubro().getDetalle();
                o[4] = co.getTotal();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void limpiarCampos() {
        deTxt.setText("");
        alTxt.setText("");
        filtroTxt.setText("");
        combo.removeAllItems();
        combo.addItem("");
    }
