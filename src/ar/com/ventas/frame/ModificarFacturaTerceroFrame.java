    private void guardar() {
        if (validar()) {
            if (cobradoChk.isSelected()) {
                factura.setCobrada(true);
                Double pesos = Double.valueOf(totalTxt.getText().replace(",", "."));
                factura.setTotal(pesos);
                factura.setImporteCobrado(pesos);
            } else {
                factura.setCobrada(false);
                Double pesos = Double.valueOf(totalTxt.getText().replace(",", "."));
                Double pesosCobrados = Double.valueOf(cobradoTxt.getText().replace(",", "."));
                factura.setTotal(pesos);
                factura.setImporteCobrado(pesosCobrados);
            }
            factura.setConsorcio(consorcio);
            if (entregadaChk.isSelected()) {
                factura.setEntregada(true);
            } else {
                factura.setEntregada(false);
            }
            Date fecha = new Date();
            try {
                fecha = sdf.parse(fechaTxt.getText());
            } catch (ParseException ex) {
                Logger.getLogger(ModificarFacturaTerceroFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro 266 FECHA FACTURA\nUTILICE DD/MM/AAAA");
                return;
            }
            factura.setFecha(fecha);
            factura.setProveedor(proveedorTxt.getText());
            Integer numero = Integer.valueOf(numeroTxt.getText());
            factura.setNumero(numero);
            try {
                new FacturaDeTerceroService().updateFacturaDeTercero(factura);
                JOptionPane.showMessageDialog(this, "FACTURA ACTUALIZADA CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(ModificarFacturaTerceroFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro.289 GUARDANDO FACTURA");
                return;
            }
            volver();
        }
    }

    private void volver() {
        FacturasTerceroFrame ftf = new FacturasTerceroFrame();
        ftf.setVisible(true);
        this.dispose();
    }

    private void llenarCampos() {
        consorcioTxt.setText(consorcio.getNombre() + " "
                + consorcio.getDomicilio().getCalle() + " "
                + consorcio.getDomicilio().getNumero());
        consorcioTxt.setEditable(false);
        if (factura.getEntregada()) {
            entregadaChk.setSelected(true);
        } else {
            entregadaChk.setSelected(false);
        }
        if (factura.getCobrada()) {
            cobradoChk.setSelected(true);
        } else {
            cobradoChk.setSelected(false);
        }
        proveedorTxt.setText(factura.getProveedor());
        totalTxt.setText(df.format(factura.getTotal()));
        fechaTxt.setText(sdf.format(factura.getFecha()));
        numeroTxt.setText(factura.getNumero().toString());
        fechaTxt.requestFocus();
        cobradoTxt.setText(df.format(factura.getImporteCobrado()));
    }

    private boolean validar() {
        if (fechaTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE ESCRIBIR UNA FECHA");
            fechaTxt.requestFocus();
            return false;
        }
        if (numeroTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE ESCRIBIR UN NUMERO DE FACTURA");
            numeroTxt.requestFocus();
            return false;
        }
        if (proveedorTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE ESCRIBIR UN PROVEEDOR DE FACTURA");
            proveedorTxt.requestFocus();
            return false;
        }
        if (totalTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE ESCRIBIR UN TOTAL DE FACTURA");
            totalTxt.requestFocus();
            return false;
        }
        return true;
    }
