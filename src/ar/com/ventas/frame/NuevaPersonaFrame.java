    private void llenarCombo() {
        combo.removeAllItems();
        combo.addItem("");
        for (EstadoCivil e : EstadoCivil.values()) {
            combo.addItem(e.getEstado());
            listaEstadoCivil.add(e);
        }
    }

    private void volver() {
        AbmPersonaFrame apf = new AbmPersonaFrame();
        apf.setVisible(true);
        this.dispose();
    }

    private void guardar() {
        if (validar()) {
            Persona persona = new Persona();
            Domicilio domicilio = new Domicilio();
            domicilio.setCalle(calleTxt.getText());
            domicilio.setNumero(numeroTxt.getText());
            domicilio.setPisoDto(pisoDtoTxt.getText());
            domicilio.setCodigoPostal(codigoPostalTxt.getText());
            domicilio.setLocalidad(localidadTxt.getText());
            domicilio.setProvincia(provinciaTxt.getText());
            persona.setActivo(true);
            persona.setApellidoNombre(apellidoNombreTxt.getText());
            persona.setApellidoNombrePareja(apellidoNombreParejaTxt.getText());
            persona.setClaveFiscal(claveFiscalTxt.getText());
            persona.setCodigo(codigo);
            persona.setCuil(cuilTxt.getText());
            persona.setDni(dniTxt.getText());
            persona.setDniPareja(dniParejaTxt.getText());
            persona.setDomicilio(domicilio);
            if (esEmpleadoChk.isSelected()) {
                persona.setEsEmpleado(true);
            } else {
                persona.setEsEmpleado(false);
            }
            if (esTitularCuitChk.isSelected()) {
                persona.setEsTitularCuit(true);
            } else {
                persona.setEsTitularCuit(false);
            }
            int row = combo.getSelectedIndex();
            int cod = listaEstadoCivil.get(row - 1).getCodigo();
            persona.setEstadoCivil(cod);
            Date fn = new Date();
            try {
                fn = sdf.parse(fechaNacimientoTxt.getText());
            } catch (ParseException ex) {
                Logger.getLogger(NuevaPersonaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            persona.setFechaNacimiento(fn);
            Date fb = new Date();
            try {
                fb = sdf.parse("01/01/1900");
            } catch (ParseException ex) {
                Logger.getLogger(NuevaPersonaFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            persona.setFechaBaja(fb);
            Date fi = new Date();
            if (!fechaIngresoTxt.getText().isEmpty()) {
                try {
                    fi = sdf.parse(fechaIngresoTxt.getText());
                } catch (ParseException ex) {
                    Logger.getLogger(NuevaPersonaFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                try {
                    fi = sdf.parse("01/01/1900");
                } catch (ParseException ex) {
                    Logger.getLogger(NuevaPersonaFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            persona.setFechaIngreso(fi);
            Date fnp = new Date();
            if (!fechaNacimientoParejaTxt.getText().isEmpty()) {
                try {
                    fnp = sdf.parse(fechaNacimientoParejaTxt.getText());
                } catch (ParseException ex) {
                    Logger.getLogger(NuevaPersonaFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                try {
                    fnp = sdf.parse("01/01/1900");
                } catch (ParseException ex) {
                    Logger.getLogger(NuevaPersonaFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            persona.setFechaNacimientoPareja(fnp);
            persona.setMail(mailTxt.getText());
            persona.setObservaciones(observacionesTxt.getText());
            persona.setTelefono(telefonoTxt.getText());
            if (esTitularCuitChk.isSelected()) {
                JOptionPane.showMessageDialog(this, "RECUERDE CARGAR DATOS DE TITULAR DE CUIT");
            }
            if (esEmpleadoChk.isSelected()) {
                JOptionPane.showMessageDialog(this, "RECUERDE CARGAR DATOS EMPLEADO");
            }
            try {
                new PersonaService().savePersona(persona);
                JOptionPane.showMessageDialog(this, "PERSONA GUARDADA CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(NuevaPersonaFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro. 856 GUARDANDO PERSONA");
                return;
            }
            volver();
        }
    }

    private boolean validar() {
        if (apellidoNombreTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR EL APELLIDO Y NOMBRE DE LA PERSONA");
            apellidoNombreTxt.requestFocus();
            return false;
        }
        if (calleTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UNA CALLE PARA EL DOMICILIO DE LA PERSONA");
            calleTxt.requestFocus();
            return false;
        }
        if (numeroTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UN NUMERO PARA EL DOMICILIO DE LA PERSONA");
            numeroTxt.requestFocus();
            return false;
        }
        if (localidadTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UNA LOCALIDAD PARA EL DOMICILIO DE LA PERSONA");
            localidadTxt.requestFocus();
            return false;
        }
        if (provinciaTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UNA PROVINCIA PARA EL DOMICILIO DE LA PERSONA");
            provinciaTxt.requestFocus();
            return false;
        }
        if (dniTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR EL DNI DE LA PERSONA");
            dniTxt.requestFocus();
            return false;
        }
        if (fechaNacimientoTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR LA FECHA DE NACIMIENTO DE LA PERSONA");
            fechaNacimientoTxt.requestFocus();
            return false;
        }
        int row = combo.getSelectedIndex();
        if (row < 1) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN ESTADO CIVIL");
            combo.requestFocus();
            return false;
        }
        if (esEmpleadoChk.isSelected()) {
            if (cuilTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "COMO ES EMPLEADO DEBE INGRESAR SU CUIL");
                cuilTxt.requestFocus();
                return false;
            }
            if (fechaIngresoTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "COMO ES EMPLEADO DEBE INGRESAR FECHA INGRESO");
                fechaIngresoTxt.requestFocus();
                return false;
            }
        }
        return true;
    }
