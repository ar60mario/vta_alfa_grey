    private void fechaConsultaTxtKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fechaConsultaTxtKeyPressed
        if (evt.getKeyCode() == 10) {
            String fe = fechaConsultaTxt.getText();
            int largo = fe.length();
            if (largo == 10) {
                consultar(fe);
            } else {
                if (largo > 10) {
                    JOptionPane.showMessageDialog(this, "ERROR EN LARGO DE FECHA");
                    return;
                }
            }
            fe = UtilFrame.fecha(fe);
            fechaConsultaTxt.setText(fe);
        }


    }//GEN-LAST:event_fechaConsultaTxtKeyPressed

