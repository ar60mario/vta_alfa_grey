    private void limpiarFrame() {
        UtilFrame.limpiarTabla(tabla);
        llenarTabla();
    }

    private void nuevo() {
        FondoReciboNuevoFrame frnf = new FondoReciboNuevoFrame();
        frnf.setVisible(true);
        this.dispose();
    }

    private void modificar() {
        int row = tabla.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN FONDO");
            return;
        }
        FondoRecibo fr = fondos.get(row);
        FondoReciboModificarFrame frmf = new FondoReciboModificarFrame(fr);
        frmf.setVisible(true);
        this.dispose();
    }

    private void eliminar() {
        int row = tabla.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN FONDO");
            return;
        }
        FondoRecibo fr = fondos.get(row);
        fr.setActivo(false);
        int a = JOptionPane.showConfirmDialog(this, "CONFIRMA DESACTIVAR?", "Atención", JOptionPane.YES_NO_OPTION);
        if(a==0){
            try {
                new FondoReciboService().updateFondoRecibo(fr);
            } catch (Exception ex) {
                Logger.getLogger(AbmFondosRecibosFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR 249 - INACTIVO");
            return;
            }
            JOptionPane.showMessageDialog(this, "DESACTIVADO");
            limpiarFrame();
        }
    }

    private void eliminados() {
        FondosReciboEliminadosFrame fref = new FondosReciboEliminadosFrame();
        fref.setVisible(true);
        this.dispose();
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void llenarTabla() {
        fondos = null;
        try {
            fondos = new FondoReciboService().getAllFondoRecibosActivos();
        } catch (Exception ex) {
            Logger.getLogger(AbmFondosRecibosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (fondos != null && !fondos.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (FondoRecibo fr : fondos) {
                Object o[] = new Object[3];
                o[0] = fr.getEmpresa();
                o[1] = fr.getUbicacion();
                o[2] = fr.getTitular().getPersona().getApellidoNombre();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }
