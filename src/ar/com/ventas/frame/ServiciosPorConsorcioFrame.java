    private void llenarCombo() {
        consorcios = null;
        combo.removeAllItems();
        combo.addItem("");
        try {
            consorcios = new ConsorcioService().getAllConsorciosActivos();
        } catch (Exception ex) {
            Logger.getLogger(ServiciosPorConsorcioFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (consorcios != null && !consorcios.isEmpty()) {
            for (Consorcio conso : consorcios) {
                Domicilio dm = conso.getDomicilio();
                String cons = dm.getCalle() + " " + dm.getNumero();
                combo.addItem(cons);
            }
        }
    }

    private void llenarTabla(Consorcio consorcio) {
        UtilFrame.limpiarTabla(tabla);
        List<Servicio> servicios = null;
        try {
            servicios = new ServicioService().getAllServiciosActivosByConsorcio(consorcio);
        } catch (Exception ex) {
            Logger.getLogger(ServiciosPorConsorcioFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
        if (servicios != null && !servicios.isEmpty()) {
            for (Servicio serv : servicios) {
                Object o[] = new Object[5];
                Domicilio dm = serv.getConsorcio().getDomicilio();
                String conso = dm.getCalle() + " " + dm.getNumero();
                o[0] = sdf.format(serv.getFecha());
                o[1] = conso;
                o[2] = serv.getRubro().getDetalle();
                o[3] = "";
                o[4] = "";
                tbl.addRow(o);
            }
        }
        tabla.setModel(tbl);
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }
