    private void modificar() {
        int rowT = tabla.getSelectedRow();
        Servicio t = servicios.get(rowT);
        t.setActivo(true);
        int a = JOptionPane.showConfirmDialog(this, "CONFIRMA ACTIVAR\n" 
                + t.getConsorcio().getNombre() + " "
                + t.getConsorcio().getDomicilio().getCalle() + " "
                + t.getConsorcio().getDomicilio().getNumero(),
                 "AtenciÃ³n", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            try {
                new ServicioService().updateServicio(t);
                JOptionPane.showMessageDialog(this, "CONSORCIO ACTIVADO CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(ServiciosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR ACTIVANDO CONSORCIO");
                return;
            }
            volver();
        }
    }

    private void volver() {
        ServiciosFrame mf = new ServiciosFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void llenarCombos() {
        consorcios = null;
        try {
            consorcios = new ConsorcioService().getAllConsorciosActivos();
        } catch (Exception ex) {
            Logger.getLogger(ServiciosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        rubros = null;
        try {
            rubros = new RubroService().getAllRubrosActivos();
        } catch (Exception ex) {
            Logger.getLogger(ServiciosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (consorcios != null && !consorcios.isEmpty()) {
            for (Consorcio c : consorcios) {
                String co = c.getNombre() + " "
                        + c.getDomicilio().getCalle() + " "
                        + c.getDomicilio().getNumero();
            }
        }
        if (rubros != null && !rubros.isEmpty()) {
            for (Rubro s : rubros) {
                String co = s.getDetalle();
            }
        }
        llenarTabla();
    }

    private void llenarTabla() {
        servicios = null;
        limpiarTabla();
        try {
            servicios = new ServicioService().getAllServiciosInactivos();
        } catch (Exception ex) {
            Logger.getLogger(ServiciosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (servicios != null && !servicios.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Servicio t : servicios) {
                Object o[] = new Object[4];
                o[0] = t.getConsorcio().getDomicilio().getCalle() + " "
                        + t.getConsorcio().getDomicilio().getNumero();
                o[1] = t.getRubro().getDetalle();
                o[2] = t.getCantidadCuotas();
                o[3] = t.getPeriodicidad();
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void limpiarTabla() {
        int rows = tabla.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (int i = 0; i < rows; i++) {
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }
