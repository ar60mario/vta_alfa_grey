    private void llenarTabla() {
        UtilFrame.limpiarTabla(tabla);
        categorias = null;
        try {
            categorias = new CategoriaMonotributoService().getAllCategoriasMonotributoActivos();
        } catch (Exception ex) {
            Logger.getLogger(AbmMonotributoFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (categorias != null && !categorias.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (CategoriaMonotributo cm : categorias) {
                Object o[] = new Object[4];
                o[0] = cm.getCategoria();
                o[1] = df.format(cm.getIngresosBrutos());
                if (cm.getActivo()) {
                    o[2] = "ACTIVO";
                } else {
                    o[2] = "INACTIVO";
                }
                o[3] = sdf.format(cm.getFechaActualizacion());
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void nueva() {
        NuevaCategoriaMonotributoFrame ncmf = new NuevaCategoriaMonotributoFrame();
        ncmf.setVisible(true);
        this.dispose();
    }

    private void grabar() {
        int rows = tabla.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (int i = 0; i < rows; i++) {
                Double im = Double.valueOf(tbl.getValueAt(i, 1).toString().replace(",", "."));
                CategoriaMonotributo c = categorias.get(i);
                c.setIngresosBrutos(im);
                try {
                    new CategoriaMonotributoService().updateCategoriaMonotributo(c);
                } catch (Exception ex) {
                    Logger.getLogger(AbmMonotributoFrame.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showMessageDialog(this, "ERROR Nro. 264 GUARDANDO CATEGORIAS");
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, "CATEGORIAS GUARDADAS CORRECTAMENTE");
            volver();
        }
    }

    private void desactivar() {
        int row = tabla.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN CATEGORIA PARA DESACTIVAR");
            return;
        }
        CategoriaMonotributo cm = categorias.get(row);
        cm.setActivo(false);
        int a = JOptionPane.showConfirmDialog(this, "CONFIRME DESACTIVAR CATEORIA \n" + cm.getCategoria(), "AtenciÃ³n", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            try {
                new CategoriaMonotributoService().updateCategoriaMonotributo(cm);
                JOptionPane.showMessageDialog(this, "CATEGORIA ACTUALIZADA CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(AbmMonotributoFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro.257 ACTUALIZANDO CATEGORIA");
                return;
            }
            llenarTabla();
        }
    }

    private void inactivas() {
        CategoriaMonotributoInactivasFrame cmif = new CategoriaMonotributoInactivasFrame();
        cmif.setVisible(true);
        this.dispose();
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }
