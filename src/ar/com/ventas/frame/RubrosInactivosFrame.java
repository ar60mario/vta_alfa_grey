    private void llenarTabla() {
        limpiarTabla();
        rubros = null;
        try {
            rubros = new RubroService().getAllRubrosInactivos();
        } catch (Exception ex) {
            Logger.getLogger(RubrosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (rubros != null && !rubros.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Rubro s : rubros) {
                Object o[] = new Object[4];
                o[0] = s.getDetalle();
                o[1] = s.getEstructura().getNombre();
                o[2] = df.format(s.getImporte());
                if (s.getActivo()) {
                    o[3] = "ACTIVO";
                } else {
                    o[3] = "INACTIVO";
                }
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void limpiarTabla() {
        int rows = tabla.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (int i = 0; i < rows; i++) {
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }

    private void volver() {
        AbmRubrosFrame mf = new AbmRubrosFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void activar() {
        int row = tabla.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "ELIJA UN RUBRO PARA ACTIVAR");
            return;
        }
        Rubro s = rubros.get(row);
        s.setActivo(true);
        try {
            new RubroService().updateRubro(s);
            JOptionPane.showMessageDialog(this, "RUBRO ACTIVADO CORRECTAMENTE");
        } catch (Exception ex) {
            Logger.getLogger(RubrosInactivosFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "ERROR Nro. 227 ACTIVANDO RUBRO");
            return;
        }
        volver();
    }
