    private void volver() {
        VerFacturasParaAjustarPeriodoFrame vfpapf = new VerFacturasParaAjustarPeriodoFrame();
        vfpapf.setVisible(true);
        this.dispose();
    }

    private void grabar() {
        Integer cuo = Integer.valueOf(cuotaTxt.getText());
        String perio = periodoTxt.getText();
        co.setPeriodo(perio);
        co.setCuotasPagadas(cuo);
        try {
            new ComprobanteService().updateComprobante(co);
        } catch (Exception ex) {
            Logger.getLogger(ComprobanteParaModificarPeriodoFrame.class.getName()).log(Level.SEVERE, null, ex);
            return;
        }
        volver();
    }

    private void llenarFrame() {
        cuotaTxt.setText(co.getCuotasPagadas().toString());
        periodoTxt.setText(co.getPeriodo());
        nombreTxt.setText(co.getCalleNroPisoDtoCliente());
        rubroTxt.setText(co.getRubro().getDetalle());
    }
