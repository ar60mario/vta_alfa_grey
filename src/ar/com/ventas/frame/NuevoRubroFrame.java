    private void limpiarCampos() {
        llenarCombo();
        codigo = 0;
        int o = 0;
        try {
            codigo = new RubroService().getCodigoSiguiente();
            if (codigo != null) {
                o = 1;
            } else {
                o = 0;
            }
        } catch (Exception ex) {
            Logger.getLogger(NuevoRubroFrame.class.getName()).log(Level.SEVERE, null, ex);
            o = 0;
        }
        if (o == 0) {
            codigo = 0;
        }
        codigo += 1;
        detalleTxt.setText("");
        textoEnFacturaTxt.setText("");
        esAbonoChk.setSelected(false);
    }

    private void llenarCombo() {
        limpiarTabla();
        combo.removeAllItems();
        combo.addItem("");
        estructuras = null;
        try {
            estructuras = new EstructuraService().getAllEstructurasActivas();
        } catch (Exception ex) {
            Logger.getLogger(NuevoRubroFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (estructuras != null && !estructuras.isEmpty()) {
            for (Estructura est : estructuras) {
                combo.addItem(est.getNombre());
            }
        }
    }

    private void llenarTabla() {
        int row = combo.getSelectedIndex();
        if (row > 0) {
            limpiarTabla();
            Estructura estructura = estructuras.get(row - 1);
            List<EstructuraServicio> campos = null;
            try {
                campos = new EstructuraServicioService().getAllEstructuraServicioByEstructuraActivos(estructura);
            } catch (Exception ex) {
                Logger.getLogger(NuevoRubroFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (campos != null && !campos.isEmpty()) {
                DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
                for (EstructuraServicio es : campos) {
                    Object o[] = new Object[1];
                    o[0] = es.getContenido();
                    tbl.addRow(o);
                }
                tabla.setModel(tbl);
            }
        }
    }

    private void limpiarTabla() {
        int rows = tabla.getRowCount();
        if (rows > 0) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (int i = 0; i < rows; i++) {
                tbl.removeRow(0);
            }
            tabla.setModel(tbl);
        }
    }

    private void guardar() {
        if (validar()) {
            int row = combo.getSelectedIndex();
            Estructura estructura = estructuras.get(row - 1);
            Rubro rubro = new Rubro();
            rubro.setActivo(true);
            rubro.setTextoEnFactura(textoEnFacturaTxt.getText());
            rubro.setCodigo(codigo);
            rubro.setDetalle(detalleTxt.getText());
            rubro.setEstructura(estructura);
            Double importe = Double.valueOf(textoEnFacturaTxt.getText());
            rubro.setImporte(importe);
            if(esAbonoChk.isSelected()){
                rubro.setEsAbono(true);
            } else {
                rubro.setEsAbono(false);
            }
            try {
                new RubroService().saveRubro(rubro);
                JOptionPane.showMessageDialog(this, "RUBRO GUARDADO CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(NuevoRubroFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro.312 GUARDANDO RUBRO");
                return;
            }
            volver();
        }
    }

    private void volver() {
        AbmRubrosFrame asf = new AbmRubrosFrame();
        asf.setVisible(true);
        this.dispose();
    }

    private boolean validar() {
        int row = combo.getSelectedIndex();
        if (row < 1) {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA ESTRUCTURA PARA ESTE RUBRO");
            return false;
        }
        return true;
    }
