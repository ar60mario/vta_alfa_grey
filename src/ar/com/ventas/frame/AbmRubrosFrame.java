    private void llenarTabla() {
        UtilFrame.limpiarTabla(tabla);
        rubros = null;
        try {
            rubros = new RubroService().getAllRubrosActivos();
        } catch (Exception ex) {
            Logger.getLogger(AbmRubrosFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (rubros != null && !rubros.isEmpty()) {
            DefaultTableModel tbl = (DefaultTableModel) tabla.getModel();
            for (Rubro s : rubros) {
                Object o[] = new Object[5];
                o[0] = s.getDetalle();
                o[1] = s.getEstructura().getNombre();
                o[2] = df.format(s.getImporte());
                if (s.getActivo()) {
                    o[3] = "ACTIVO";
                } else {
                    o[3] = "INACTIVO";
                }
                if(s.getEsAbono() != null){
                    if(s.getEsAbono()){
                        o[4] = "ES ABONO";
                    } else {
                        o[4] = "REPARACION";
                    }
                } else {
                    o[4] = "REPARACION";
                }
                tbl.addRow(o);
            }
            tabla.setModel(tbl);
        }
    }

    private void volver() {
        MainFrame mf = new MainFrame();
        mf.setVisible(true);
        this.dispose();
    }

    private void inactivos() {
        RubrosInactivosFrame sif = new RubrosInactivosFrame();
        sif.setVisible(true);
        this.dispose();
    }

    private void modificar() {
        int row = tabla.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UN SERVICIO PARA MODIFICAR");
            return;
        }
        Rubro servicio = rubros.get(row);
        ModificarRubroFrame msf = new ModificarRubroFrame(servicio);
        msf.setVisible(true);
        this.dispose();
    }

    private void nuevo() {
        NuevoRubroFrame nsf = new NuevoRubroFrame();
        nsf.setVisible(true);
        this.dispose();
    }
