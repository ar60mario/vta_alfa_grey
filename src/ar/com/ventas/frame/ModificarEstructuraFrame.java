    private void guardar() {
        if (validar()) {
            if (activoChk.isSelected()) {
                estructura.setActivo(true);
            } else {
                estructura.setActivo(false);
            }
            estructura.setNombre(nombreTxt.getText());
            estructura.setTextoFactura(textoFacturaTxt.getText());
            estructura.setObservaciones(observacionesTxt.getText());
            try {
                new EstructuraService().updateEstructura(estructura);
                JOptionPane.showMessageDialog(this, "ESTRUCTURA GUARDADA CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(ModificarEstructuraFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro. 198 GUARDANDO ESTRUCTURA");
                return;
            }
            volver();
        }
    }

    private void volver() {
        CrearEstructuraFrame cef = new CrearEstructuraFrame();
        cef.setVisible(true);
        this.dispose();
    }

    private boolean validar() {
        if (nombreTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UN NOMBRE PARA ESTA ESTRUCTURA");
            return false;
        }
        return true;
    }

    private void llenarCampos() {
        observacionesTxt.setText(estructura.getObservaciones());
        nombreTxt.setText(estructura.getNombre());
        if (estructura.getTextoFactura() == null) {
            textoFacturaTxt.setText("");
        } else {
            textoFacturaTxt.setText(estructura.getTextoFactura());
        }
        if (estructura.getActivo()) {
            activoChk.setSelected(true);
        } else {
            activoChk.setSelected(false);
        }
    }
