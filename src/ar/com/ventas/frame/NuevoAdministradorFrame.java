    private void guardar() {
        if (validar()) {
            Administrador adm = new Administrador();
            adm.setActivo(true);
            adm.setCodigo(codigo);
            adm.setContacto(contactoTxt.getText());
            adm.setContacto1(contacto1Txt.getText());
            adm.setContacto2(contacto2Txt.getText());
            adm.setCuit(cuitTxt.getText());
            Domicilio domicilio = new Domicilio();
            domicilio.setCalle(calleTxt.getText());
            domicilio.setNumero(numeroTxt.getText());
            domicilio.setPisoDto(pisoDtoTxt.getText());
            domicilio.setCodigoPostal(codigoPostalTxt.getText());
            domicilio.setLocalidad(localidadTxt.getText());
            domicilio.setProvincia(provinciaTxt.getText());
            adm.setDomicilio(domicilio);
            adm.setMail(mailTxt.getText());
            adm.setNombreAdministrador(nombreAdministradorTxt.getText());
            adm.setObservaciones(observacionesTxt.getText());
            adm.setRazonSocial(razonSocialTxt.getText());
            adm.setTelefono(celularTxt.getText());
            adm.setTelefono1(celular1Txt.getText());
            adm.setTelefono2(celular2Txt.getText());
            adm.setTelefonoAdministracion(telefonoAdministracionTxt.getText());
            try {
                adm = new AdministradorService().saveAdministrador(adm);
                JOptionPane.showMessageDialog(this, "ADMINISTRADOR GUARDADO CORRECTAMENTE");
            } catch (Exception ex) {
                Logger.getLogger(NuevoAdministradorFrame.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "ERROR Nro.751 GUARDANDO ADMINISTRADOR");
                return;
            }
            volver();
        }
    }

    private void volver() {
        AbmAdministradorFrame aaf = new AbmAdministradorFrame();
        aaf.setVisible(true);
        this.dispose();
    }

    private void buscarSiguiente() {
        codigo = 0;
        int o = 0;
        try {
            codigo = new AdministradorService().getCodigoSiguiente();
            if (codigo != null) {
                o = 1;
            } else {
                o = 0;
            }
        } catch (Exception ex) {
            Logger.getLogger(NuevoAdministradorFrame.class.getName()).log(Level.SEVERE, null, ex);
            o = 0;
        }
        if (o == 0) {
            codigo = 0;
        }
        codigo += 1;
    }

    private boolean validar() {
        if (razonSocialTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR UNA RAZON SOCIAL");
            razonSocialTxt.requestFocus();
            return false;
        }
        if (nombreAdministradorTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR EL NOMBRE DEL ADMINISTRADOR");
            nombreAdministradorTxt.requestFocus();
            return false;
        }
        if (!cuitTxt.getText().isEmpty()) {
            int largo = cuitTxt.getText().length();
            if (largo != 13) {
                JOptionPane.showMessageDialog(this, "ERROR EN LA CUIT UTILICE LOS GUIONES");
                cuitTxt.requestFocus();
                return false;
            }
        }
        if (calleTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR LA CALLE DEL ADMINISTRADOR");
            calleTxt.requestFocus();
            return false;
        }
        if (numeroTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR EL NUMERO DEL DOMICILIO DEL ADMINISTRADOR");
            numeroTxt.requestFocus();
            return false;
        }
        if (localidadTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR LA LOCALIDAD DEL DOMICILIO DEL ADMINISTRADOR");
            localidadTxt.requestFocus();
            return false;
        }
        if (provinciaTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR LA PROVINCIA DEL DOMICILIO DEL ADMINISTRADOR");
            provinciaTxt.requestFocus();
            return false;
        }
        if (telefonoAdministracionTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "DEBE INGRESAR EL TELEFONO DE LA ADMINISTRACION");
            telefonoAdministracionTxt.requestFocus();
            return false;
        }
        return true;
    }
