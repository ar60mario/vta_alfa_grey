package ar.com.ventas.entities;

/**
 *
 * @author Mario
 */
public class TicketToken {
    private Long id;
    private String token;
    private String sign;

    public TicketToken() {
    }

    public TicketToken(Long id, String token, String sign) {
        this.id = id;
        this.token = token;
        this.sign = sign;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getSign() {
        return sign;
    }

    public void setSign(String sign) {
        this.sign = sign;
    }
    
    
}
